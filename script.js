const sliderTrack = document.querySelector("#slider-track");
const sliderDots = document.querySelector("#slider-dots");
const portfolioGrid = document.querySelector("#portfolio-grid");
const modal = document.querySelector("#image-modal");
const modalImage = modal.querySelector("img");
const modalClose = modal.querySelector(".modal-close");
const scrollTopButton = document.querySelector(".scroll-top");
const navToggle = document.querySelector(".nav-toggle");
const navMenu = document.querySelector(".nav-menu");
const langButtons = document.querySelectorAll(".lang-btn");

let currentLanguage = new URLSearchParams(window.location.search).get("lang") === "es" ? "es" : "en";
let currentSlide = 0;
let sliderTimer;

function getValue(object, path) {
  return path.split(".").reduce((accumulator, key) => accumulator?.[key], object);
}

function setMetaContent(selector, value) {
  const element = document.querySelector(selector);
  if (element) {
    if (selector === "title") {
      document.title = value;
    } else {
      element.setAttribute("content", value);
    }
  }
}

function renderSlider(language) {
  sliderTrack.innerHTML = "";
  sliderDots.innerHTML = "";

  translations[language].slider.forEach((item, index) => {
    const slide = document.createElement("article");
    slide.className = `slide ${index === 0 ? "is-active" : ""}`;
    slide.innerHTML = `
      <img src="${sliderImages[index]}" alt="${item.title}">
      <div class="slide-caption">
        <strong>${item.title}</strong>
        <span>${item.text}</span>
      </div>
    `;
    sliderTrack.appendChild(slide);

    const dot = document.createElement("button");
    dot.className = `slider-dot ${index === 0 ? "is-active" : ""}`;
    dot.type = "button";
    dot.setAttribute("aria-label", `Slide ${index + 1}`);
    dot.addEventListener("click", () => {
      currentSlide = index;
      updateSlider();
      restartSliderTimer();
    });
    sliderDots.appendChild(dot);
  });

  currentSlide = 0;
}

function updateSlider() {
  const slides = document.querySelectorAll(".slide");
  const dots = document.querySelectorAll(".slider-dot");
  slides.forEach((slide, index) => slide.classList.toggle("is-active", index === currentSlide));
  dots.forEach((dot, index) => dot.classList.toggle("is-active", index === currentSlide));
}

function startSlider() {
  sliderTimer = window.setInterval(() => {
    const total = document.querySelectorAll(".slide").length;
    currentSlide = (currentSlide + 1) % total;
    updateSlider();
  }, 4500);
}

function restartSliderTimer() {
  window.clearInterval(sliderTimer);
  startSlider();
}

function renderPortfolio(language) {
  portfolioGrid.innerHTML = "";

  translations[language].portfolioItems.forEach((item, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "portfolio-card";
    button.innerHTML = `
      <img src="${portfolioImages[index]}" alt="${item.title}">
      <div class="portfolio-meta">
        <strong>${item.title}</strong>
        <span>${item.text}</span>
      </div>
    `;
    button.addEventListener("click", () => openModal(portfolioImages[index], item.title));
    portfolioGrid.appendChild(button);
  });
}

function openModal(src, alt) {
  modalImage.src = src;
  modalImage.alt = alt;
  modal.classList.add("is-open");
  modal.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeModal() {
  modal.classList.remove("is-open");
  modal.setAttribute("aria-hidden", "true");
  modalImage.src = "";
  modalImage.alt = "";
  document.body.style.overflow = "";
}

function applyTranslations(language) {
  currentLanguage = language;
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(translations[language], element.dataset.i18n);
    if (value) {
      element.textContent = value;
    }
  });

  setMetaContent("title", translations[language].meta.title);
  setMetaContent('meta[name="description"]', translations[language].meta.description);
  setMetaContent('meta[property="og:title"]', translations[language].meta.ogTitle);
  setMetaContent('meta[property="og:description"]', translations[language].meta.ogDescription);

  langButtons.forEach((button) => button.classList.toggle("is-active", button.dataset.lang === language));
  renderSlider(language);
  renderPortfolio(language);
  restartSliderTimer();

  const url = new URL(window.location.href);
  if (language === "es") {
    url.searchParams.set("lang", "es");
  } else {
    url.searchParams.delete("lang");
  }
  window.history.replaceState({}, "", url);
}

navToggle.addEventListener("click", () => {
  const open = navMenu.classList.toggle("is-open");
  navToggle.setAttribute("aria-expanded", String(open));
});

document.querySelectorAll(".nav-menu a").forEach((link) => {
  link.addEventListener("click", () => {
    navMenu.classList.remove("is-open");
    navToggle.setAttribute("aria-expanded", "false");
  });
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => applyTranslations(button.dataset.lang));
});

scrollTopButton.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

window.addEventListener("scroll", () => {
  scrollTopButton.classList.toggle("is-visible", window.scrollY > 400);
});

modal.addEventListener("click", (event) => {
  if (event.target === modal) {
    closeModal();
  }
});

modalClose.addEventListener("click", closeModal);

window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && modal.classList.contains("is-open")) {
    closeModal();
  }
});

applyTranslations(currentLanguage);
